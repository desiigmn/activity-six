// CONVERT STRING TO A NUMBER
let price = "200";
// parseInt()
let a = parseInt(price);
console.log("Using parseInt():", a);
// Number()
let b = Number(price);
console.log("Using Number():", b);
// + Operator
let c = +price;
console.log("Using + operator:", c);


// CONVERT NUMBER TO A STRING
let distance = 150;
// .toString()
let y = distance.toString();
console.log("Using toString():", y);
// String()
let x = String(distance);
console.log("Using String():", x);


// CONVERT STRING TO A DECIMAL NUMBER
let weight = "72.5";
// parseFloat()
let z = parseFloat(weight);
console.log("Using parseFloat():", z);